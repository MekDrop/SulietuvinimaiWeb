  str=str.replace("%7C","|");
  str=str.replace("%5B","[");
  str=str.replace("%5D","]");
  str=str.replace("%7B","{");
  str=str.replace("%7D","}");
  str=str.replace("%3A",":");
  str=str.replace("%3B",";");
  str=str.replace("%22",'"'); 
  str=str.replace("%27","'");
  str=str.replace("%3C","<");
  str=str.replace("%3E",">");
  str=str.replace("%5D","]");
  str=str.replace("%7B","{");
  str=str.replace("%7D","}");
  str=str.replace("%3A",":");
  str=str.replace("%3B",";");
  str=str.replace("%22",'"'); 
  str=str.replace("%E0","à");
  str=str.replace("%E8","è");
  str=str.replace("%E6","æ");
  str=str.replace("%EB","ë");
  str=str.replace("%E1","á");
  str=str.replace("%F0","ð");
