  str=str.replace("%24","$");
  str=str.replace("%25","%");
  str=str.replace("%5E","^");
  str=str.replace("%26","&"); 
  str=str.replace("%28","(");
  str=str.replace("%29",")");
  str=str.replace("%3D","=");
  str=str.replace("+"," ");
